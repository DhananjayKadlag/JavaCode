package com.aspire.java.string;

public class StringCase {
public static void main(String[] args)
{
	String s1="Aspire Training Institute";
	String s2="aspire";
	String s3="PUNE";
	String s4=" ";
	System.out.println(s1.toUpperCase());// convert s1 string to upper case
	System.out.println(s1.toLowerCase());// convert s1 string to lower case
	System.out.println(s2.toUpperCase());
	System.out.println(s3.toLowerCase());
	System.out.println(s1.length());
	System.out.println(s2.length());//length of string=charactercount
	System.out.println(s4.length());
}
}
